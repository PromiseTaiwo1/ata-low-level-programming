#include <stdio.h>

/**
 * min - integer variable
 * description: Stores the minimum value, set to -100.
*/
int min = -100;

/**
 * max - integer variable
 * description: Stores the maximum value, set to 100.
*/
int max = 100;

/**
 * main - the main function
 * description: Prints the values of 'min' and 'max' integer variables.
 * return: 0
*/
int main() {
    printf("Min: %d\n", min);
    printf("Max: %d\n", max);
    return 0;
}

